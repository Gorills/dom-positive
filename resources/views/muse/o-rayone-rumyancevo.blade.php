       <p>Инфраструктура ТиНАО и жизнь в Новой Москве</p>
       <p>&nbsp;</p>
       <p>С присоединением новых территорий к Москве 1 июля 2012 года площадь столицы увеличилась почти в два с половиной раза. Население города после расширения выросло на 250 000 человек, а Москва приобрела общую границу с Калужской областью, попав в рейтинг десяти крупнейших мегаполисов мира.</p>
       <p>&nbsp;</p>
       <p>Для тех москвичей, кто мечтал приобрести собственное жилье в столице, квартиры в Румянцево, Тропарево или Солнцево стали альтернативой новостроек от застройщиков в городах ближнего Подмосковья. Многие районы ТиНАО — Троицкого и Новомосковского административных округов столицы — значительно ближе к границам старой Москвы, нежели Одинцово или Мытищи. И вместе с тем к новостройке в новой Москве прилагаются выгодные ставки по ипотеке, столичная прописка, социальные льготы и пособия.</p>
       <p>&nbsp;</p>
       <p>С расширением столицы тихая и размеренная подмосковная жизнь во Внуково, Троицке и Щербинке — некогда городах, а теперь новых округах Москвы — стала более насыщенной. Большинство людей, которые жили здесь и раньше, а теперь в одно мгновение стали москвичами, позитивно оценили изменения, произошедшие после присоединения.</p>
       <p>&nbsp;</p>
       <p>От новой Москвы до центра столицы за сорок минут</p>
       <p>&nbsp;</p>
       <p>Квартиры в Солнцево и Румянцево, округах, вплотную прилегающих к МКАД, находятся ближе всего к центру. В 2016 году здесь открыли две станции метро — Саларьево и Румянцево, и добраться до Лубянки получится всего за сорок минут. Чуть дальше по киевскому шоссе находятся новостройки Внуково, но и цены от застройщиков в этом районе значительно ниже.&nbsp; В некоторых новомосковских домах на киевском шоссе можно поймать волны калужского телевидения и радио, поскольку Троицкий округ граничит с другой областью, и отсюда не то, что до центра, но даже до квартиры в Тропарево придется ехать минимум час. Но в этом есть и плюсы: экология в отдаленных районах гораздо лучше, жизнь спокойнее и безопаснее. Чем дальше расположена новостройка по киевскому шоссе, тем застройщик ставит цену на нее ниже.</p>
       <p>&nbsp;</p>
       <p>Метро, электрички и новая кольцевая автодорога</p>
       <p>&nbsp;</p>
       <p>В Новой Москве хорошо развит железнодорожный транспорт: вдоль западной границы Новомосковского и Троицкого округов располагается 10 станций. В дополнение к Киевскому, здесь пролегает широкое и современное Калужское шоссе. К 2018 году в ТиНАО планируется построить новые дороги, транспортные развязки, пешеходные зоны. К 2035 году, а именно на такое время рассчитан государственный план развития новых территорий, радиальные магистрали всех новомосковских округов будут соединены поперечными дорожными линиями, которые решат важный для жителей присоединенных территорий вопрос связи между населенными пунктами.</p>
       <p>&nbsp;</p>
       <p>На строительство дорог, метро, трамвайных линий и дополнительных железнодорожных путей власти столицы ежегодно выделяют огромные суммы. К 2019 году Сокольническая линия метрополитена будет продлена еще на 4 станции: Филатов Луг, Прокшино, Ольховая, Столбово. И те, кто сегодня купил квартиры в Саларьево, уже не смогут первыми заходить в пустые вагоны метро. А к концу 2019 года откроется Центральная кольцевая автодорога (ЦКАД), 22,4 километра которой пройдут по территории Новой Москвы, вместе с ее запуском ближе к центру станут не только жители квартир на киевском шоссе, но и всех других зон округа.</p>
       <p>&nbsp;</p>
       <p>Урбанизированная среда: деловые центры, школы и больницы</p>
       <p>Согласно внутреннему административному делению новая Москва — это два административных округа, Троицкий и Новомосковский, состоящих из 21 муниципалитета. Эти округа задумывались как новый центр деловой активности столицы, способ разгрузить транспортные потоки, создать новые рабочие места, образовательные учреждения и другие объекты инфраструктуры.</p>
       <p>На присоединенных территориях Москвы сегодня формируются новые центры развития, так называемые точки роста. Это территории вокруг населенных пунктов Румянцево, поселки Мосрентген, Коммунарка, Внуково, Киевский, Щапово, Рогово, деревни Рязаново, Ярцево, город Троицк, села Вороново и Кленово. В новых центрах будут сосредоточены деловые центры, школы, развлекательная инфраструктура.</p>
       <p>Степень урбанизации зоны зависит от удаленности территории от границ со старой Москвой.&nbsp; Чем ближе к МКАД, тем плотность населения и концентрация объектов инфраструктуры выше.</p>
       <p>Зеленая новая Москва с чистым воздухом, парками и лесами</p>
       <p>&nbsp;</p>
       <p>Жители новой Москвы чаще всего отмечают природу и чистый воздух, как важное преимущество жизни здесь. Даже расположенные вплотную к кольцевой автодороге районы отделены от загазованного шумного города лесопарковыми зонами и реками. Ульяновский и говоровский лесопарки, Тропаревский лес — даже визуально на карте Москвы юго-запад выделяется на фоне серого фона множеством зеленых зон. Сегодня здесь есть как нетронутые человеком природные объекты, так и облагороженные парки, и скверы. А скоро в новой Москве появятся новые центры развлечений, в том числе горнолыжный курорт.</p>
