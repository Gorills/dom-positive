     <div class="clearfix colelem" id="u659509-11" data-muse-uid="U659509" data-muse-type="txt_frame" data-IBE-flags="txtStyleSrc" data-sizePolicy="fixed" data-pintopage="page_fixedCenter" data-leftAdjustmentDoneBy="pu659424-11"><!-- content -->
      <p id="u659509-2">Согласно внутреннему административному делению новая Москва — это два административных округа, Троицкий и Новомосковский, состоящих из 21 муниципалитета. Эти округа задумывались как новый центр деловой активности столицы, способ разгрузить транспортные потоки, создать новые рабочие места, образовательные учреждения и другие объекты инфраструктуры.</p>
      <p id="u659509-3">&nbsp;</p>
      <p id="u659509-5">На присоединенных территориях Москвы сегодня формируются новые центры развития, так называемые точки роста. Это территории вокруг населенных пунктов Румянцево, поселки Мосрентген, Коммунарка, Внуково, Киевский, Щапово, Рогово, деревни Рязаново, Ярцево, город Троицк, села Вороново и Кленово. В новых центрах будут сосредоточены деловые центры, школы, развлекательная инфраструктура.</p>
      <p id="u659509-6">&nbsp;</p>
      <p id="u659509-8">Степень урбанизации зоны зависит от удаленности территории от границ со старой Москвой.&nbsp; Чем ближе к МКАД, тем плотность населения и концентрация объектов инфраструктуры выше.</p>
      <p id="u659509-9">&nbsp;</p>
     </div>
     <!-- /m_editable -->
     <!-- m_editable region-id="editable-static-tag-U659547-BP_infinity" template="o-novoy-moskve.html" data-type="html" data-ice-options="disableImageResize,link,txtStyleTarget" -->
     <div class="clearfix colelem" id="u659547-10" data-muse-uid="U659547" data-muse-type="txt_frame" data-IBE-flags="txtStyleSrc" data-sizePolicy="fixed" data-pintopage="page_fixedCenter" data-leftAdjustmentDoneBy="pu659424-11"><!-- content -->
      <p>Жители новой Москвы чаще всего отмечают природу и чистый воздух, как важное преимущество жизни здесь. Даже расположенные вплотную к кольцевой автодороге районы отделены от загазованного шумного города лесопарковыми зонами и реками.</p>
      <p>&nbsp;</p>
      <p>Ульяновский и говоровский лесопарки, Тропаревский лес — даже визуально на карте Москвы юго-запад выделяется на фоне серого фона множеством зеленых зон. Сегодня здесь есть как нетронутые человеком природные объекты, так и облагороженные парки, и скверы.</p>
      <p>&nbsp;</p>
      <p>А скоро в новой Москве появятся новые центры развлечений, в том числе горнолыжный курорт.</p>
