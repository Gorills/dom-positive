    <div class="clearfix grpelem shared_content" id="pu659424-11" data-content-guid="pu659424-11_content"><!-- column -->
     <!-- m_editable region-id="editable-static-tag-U659424-BP_infinity" template="o-novoy-moskve.html" data-type="html" data-ice-options="disableImageResize,link,txtStyleTarget" -->
     <div class="clearfix colelem" id="u659424-11" data-muse-uid="U659424" data-muse-type="txt_frame" data-IBE-flags="txtStyleSrc" data-sizePolicy="fixed" data-pintopage="page_fixedCenter" data-leftAdjustmentDoneBy="pu659424-11"><!-- content -->
      <p id="u659424-2">С присоединением новых территорий к Москве 1 июля 2012 года площадь столицы увеличилась почти в два с половиной раза. Население города после расширения выросло на 250 000 человек, а Москва приобрела общую границу с Калужской областью, попав в рейтинг десяти крупнейших мегаполисов мира.</p>
      <p id="u659424-3">&nbsp;</p>
      <p id="u659424-5">Для тех москвичей, кто мечтал приобрести собственное жилье в столице, квартиры в Румянцево, Тропарево или Солнцево стали альтернативой новостроек от застройщиков в городах ближнего Подмосковья. Многие районы ТиНАО — Троицкого и Новомосковского административных округов столицы — значительно ближе к границам старой Москвы, нежели Одинцово или Мытищи. И вместе с тем к новостройке в новой Москве прилагаются выгодные ставки по ипотеке, столичная прописка, социальные льготы и пособия.</p>
      <p id="u659424-6">&nbsp;</p>
      <p id="u659424-8">С расширением столицы тихая и размеренная подмосковная жизнь во Внуково, Троицке и Щербинке — некогда городах, а теперь новых округах Москвы — стала более насыщенной. Большинство людей, которые жили здесь и раньше, а теперь в одно мгновение стали москвичами, позитивно оценили изменения, произошедшие после присоединения.</p>
      <p id="u659424-9">&nbsp;</p>
     </div>
     <!-- /m_editable -->
     <!-- m_editable region-id="editable-static-tag-U659446-BP_infinity" template="o-novoy-moskve.html" data-type="html" data-ice-options="disableImageResize,link,txtStyleTarget" -->
     <div class="o-novoy-moskve-textblock2 clearfix colelem" id="u659446-10" data-muse-uid="U659446" data-muse-type="txt_frame" data-IBE-flags="txtStyleSrc" data-sizePolicy="fixed" data-pintopage="page_fixedCenter" data-leftAdjustmentDoneBy="pu659424-11"><!-- content -->
      <p>Квартиры в Солнцево и Румянцево, округах, вплотную прилегающих к МКАД, находятся ближе всего к центру. В 2016 году здесь открыли две станции метро — Саларьево и Румянцево, и добраться до Лубянки получится всего за сорок минут.</p>
      <p>&nbsp;</p>
      <p>Чуть дальше по киевскому шоссе находятся новостройки Внуково, но и цены от застройщиков в этом районе значительно ниже.&nbsp; В некоторых новомосковских домах на киевском шоссе можно поймать волны калужского телевидения и радио, поскольку Троицкий округ граничит с другой областью, и отсюда не то, что до центра, но даже до квартиры в Тропарево придется ехать минимум час.</p>
      <p>&nbsp;</p>
      <p>Но в этом есть и плюсы: экология в отдаленных районах гораздо лучше, жизнь спокойнее и безопаснее. Чем дальше расположена новостройка по киевскому шоссе, тем застройщик ставит цену на нее ниже.</p>
     </div>
     <!-- /m_editable -->
     <!-- m_editable region-id="editable-static-tag-U659452-BP_infinity" template="o-novoy-moskve.html" data-type="html" data-ice-options="disableImageResize,link,txtStyleTarget" -->
     <div class="clearfix colelem" id="u659452-11" data-muse-uid="U659452" data-muse-type="txt_frame" data-IBE-flags="txtStyleSrc" data-sizePolicy="fixed" data-pintopage="page_fixedCenter" data-leftAdjustmentDoneBy="pu659424-11"><!-- content -->
      <p id="u659452-2">В Новой Москве хорошо развит железнодорожный транспорт: вдоль западной границы Новомосковского и Троицкого округов располагается 10 станций. В дополнение к Киевскому, здесь пролегает широкое и современное Калужское шоссе. К 2018 году в ТиНАО планируется построить новые дороги, транспортные развязки, пешеходные зоны. К 2035 году, а именно на такое время рассчитан государственный план развития новых территорий, радиальные магистрали всех новомосковских округов будут соединены поперечными дорожными линиями, которые решат важный для жителей присоединенных территорий вопрос связи между населенными пунктами.</p>
      <p id="u659452-3">&nbsp;</p>
      <p id="u659452-8">На строительство дорог, метро, трамвайных линий и дополнительных железнодорожных путей власти столицы ежегодно выделяют огромные суммы. К 2019 году Сокольническая линия метрополитена будет продлена еще на 4 станции: Филатов Луг, Прокшино, Ольховая, Столбово. И те, кто сегодня купил квартиры в Саларьево, уже не смогут первыми заходить в пустые вагоны метро. <br/><br/>А к концу 2019 года откроется Центральная кольцевая автодорога (ЦКАД), 22,4 километра которой пройдут по территории Новой Москвы, вместе с ее запуском ближе к центру станут не только жители квартир на киевском шоссе, но и всех других зон округа.</p>
      <p id="u659452-9">&nbsp;</p>
     </div>
     <!-- /m_editable -->
